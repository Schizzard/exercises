# Example for using string methods

my_string = "a0B1c2D3e4F5g6H7i8J9k"
print(my_string)                # a0B1c2D3e4F5g6H7i8J9k
print(my_string.capitalize())   # A0b1c2d3e4f5g6h7i8j9k
print(my_string.upper())        # A0B1C2D3E4F5G6H7I8J9K
print(my_string.lower())        # a0b1c2d3e4f5g6h7i8j9k
print(my_string[1:12:4])        # 024
print(my_string[5])             # 2

